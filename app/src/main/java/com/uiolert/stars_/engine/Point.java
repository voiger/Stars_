package com.uiolert.stars_.engine;

public class Point {
    float x;
    float y;
    float z;
    int color;
    float factorSpeed;

    public Point(float x, float y, float z, int color, float factorSpeed) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.color = color;
        this.factorSpeed = factorSpeed;
    }
}